// async action

export const getDataSymbolActionApi = (symbolId, startDate, endDate, subModelId) => {
    return async (dispatch) => {
        try {
            let url = ''
            if (subModelId == null) {
                url = `/symbols/${symbolId}/data?startDate=${startDate}&endDate=${endDate}`
            } else {
                url = `/symbols/${symbolId}/data?startDate=${startDate}&endDate=${endDate}&subModelId=${subModelId}`
            }

            const res = await http.get(url)

            if (res.status === 200) {
                const dataSymbol = res.data.data;

                if (dataSymbol.historyData && dataSymbol.forecastData) {
                    const { historyData, forecastData } = dataSymbol;
                    
                    const actionDataSymbol = { historyData, forecastData };
            
                    dispatch(setDataSymbolAction(actionDataSymbol));
                }
            }
        } catch (error) {
            console.log(error)
        }
    }
}